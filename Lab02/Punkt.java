public class Punkt {
	private double x;
	private double y;
	public Punkt(double x, double y) {
		this.x=x;
		this.y=y;
	}
	public double getX() {
		return x;
	}
	public double getY() {
		return this.y;
	}
	public void setX(double w) {
		this.x=w;
	}
	public void setY(double w) {
		this.y=w;
	}
}
